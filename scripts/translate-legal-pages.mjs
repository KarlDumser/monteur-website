const languages = Object.keys(translations);
let successCount = 0;
let errorCount = 0;

languages.forEach((lang) => {
  const filePath = path.join(localesDir, `${lang}.json`);
  
  try {
    // Read existing locale file
    const existingContent = fs.readFileSync(filePath, 'utf-8');
    const locale = JSON.parse(existingContent);

    // Deep merge translations for legal pages
    const pageKeys = Object.keys(translations[lang]);
    pageKeys.forEach((pageKey) => {
      if (!locale[pageKey]) {
        locale[pageKey] = {};
      }
      deepMerge(locale[pageKey], translations[lang][pageKey]);
    });

    // Write updated locale file
    fs.writeFileSync(filePath, JSON.stringify(locale, null, 2) + '\n', 'utf-8');
    console.log(`✅ ${lang.toUpperCase()}: Legal page translations added`);
    successCount++;
  } catch (error) {
    console.error(`❌ ${lang.toUpperCase()}: Failed to update locale - ${error.message}`);
    errorCount++;
  }
});

console.log(`\n📊 Summary: ${successCount} languages updated, ${errorCount} errors\n`);

if (errorCount === 0) {
  console.log('✨ All legal page translations completed successfully!');
  process.exit(0);
} else {
  console.log('⚠️ Some errors occurred during translation');
  process.exit(1);
}
